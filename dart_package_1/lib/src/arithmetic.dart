void main() {
  var x = 5;
  var y = 2;

  var addition = x + y;
  var subtraction = x - y;
  var multiplication = x * y;
  var division = x / y;
  var division_1 = x / y;
  var modulus = x % y;

  print('x + y = $addition');
  print('x - y = $subtraction');
  print('x * y = $multiplication');
  print('x / y = $division');
  print('x ~/ y = $division_1');
  print('x % y = $modulus');
  print('x++ = ${++x}');
  print('y-- = ${--y}');
}
